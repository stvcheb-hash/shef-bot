def calc_kbju(ingredients: list) -> dict:
    total = {"proteins": 0, "fats": 0, "carbs": 0, "calories": 0}
    for ing in ingredients:
        name = ing["name"].lower()
        grams = ing["grams"]
        found = None
        for key, vals in KBJU.items():
            if key in name or name in key:
                found = vals
                break
        if found:
            k = grams / 100
            total["proteins"] += found[0] * k
            total["fats"]     += found[1] * k
            total["carbs"]    += found[2] * k
            total["calories"] += found[3] * k
    return {k: round(v) for k, v in total.items()}